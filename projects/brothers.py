import re
def func():
    num1 = int(input("please enter number"))
    num2 = int(input("please enter number"))
    assert num2 == re.match()
    if num1 % 10 == num2 % 10 and return_check(num1) == return_check(num2):
        print("brothers")
    else:
        print("not brothers")


def return_check(n):
    while n > 10:
        n //= 10
    return n


func()
